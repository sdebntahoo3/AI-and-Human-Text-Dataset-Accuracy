import numpy as np
import pandas as pd
import torch
import gradio as gr
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.feature_extraction.text import CountVectorizer
from sklearn.preprocessing import OneHotEncoder
from sklearn.preprocessing import LabelEncoder

df = pd.read_csv('/content/sample_data/data_for_preprocessing.csv')
df.head()

X = df['Text']
y = df['Author']

vectorizer = CountVectorizer()
X_encoded = vectorizer.fit_transform(X)

encoder = LabelEncoder()
y_encoded = encoder.fit_transform(y)

X_train, X_test, y_train, y_test = train_test_split(X_encoded, y_encoded, test_size=0.2, random_state=42)

X_train_tensor = torch.from_numpy(X_train.toarray()).float()
X_test_tensor = torch.from_numpy(X_test.toarray()).float()

X_train_tensor = torch.tensor(X_train_tensor, dtype=torch.float32)
X_test_tensor = torch.tensor(X_test_tensor, dtype=torch.float32)

y_train_tensor = torch.from_numpy(y_train).float()
y_test_tensor = torch.from_numpy(y_test).float()

y_train_tensor = torch.tensor(y_train_tensor,dtype= torch.float32)
y_test_tensor = torch.tensor(y_test_tensor, dtype=torch.float32)

from torch.utils.data import Dataset,DataLoader
class CustomDataset(Dataset):
  def __init__(self, features, labels):
    self.features = features
    self.labels = labels
  def __len__(self):
    return self.features.shape[0]
  def __getitem__(self,index):
    return self.features[index], self.labels[index]

train_dataset = CustomDataset(X_train_tensor,y_train_tensor)
test_dataset = CustomDataset(X_test_tensor,y_test_tensor)

train_loader = DataLoader(train_dataset,batch_size=32,shuffle=True)
test_loader = DataLoader(test_dataset,batch_size=32,shuffle=True)

import torch.nn as nn

class MySimpleNN(nn.Module):

    def __init__(self, num_features):
        super().__init__()

        self.network = nn.Sequential(
            nn.Linear(num_features, 128),
            nn.ReLU(),

            nn.Linear(128, 64),
            nn.ReLU(),

            nn.Linear(64, 32),
            nn.ReLU(),

            nn.Linear(32, 1),
            nn.Sigmoid()
        )

    def forward(self, features):
        out = self.network(features)
        return out

learning_rate = 0.1
epochs = 25

loss_function = nn.BCELoss()

model = MySimpleNN(X_train_tensor.shape[1])

optimizer = torch.optim.SGD( model.parameters(), lr = learning_rate )

batch_size = 32
epoch = 25
n_sample = len(X_train_tensor)

for epoch in range(epochs):

  for start_indx in range(0,n_sample,batch_size):
    end_indx = start_indx + batch_size

    X_batch = X_train_tensor[start_indx:end_indx]
    y_batch = y_train_tensor[start_indx:end_indx]

    #forward pass
    y_pred = model(X_batch)
    loss = loss_function(y_pred,y_batch.reshape(-1,1))


    #upate step

    optimizer.zero_grad()
    loss.backward()
    optimizer.step()

y_pred.shape

model.eval()
accuracy_list = []

with torch.no_grad():
  for batch_features, batch_labels in test_loader:
    y_pred = model(batch_features)
    y_pred = (y_pred > 0.5).float()

    batch_accuracy = (y_pred.view(-1) == batch_labels).float().mean().item()

    accuracy_list.append(batch_accuracy)


overall_accuracy = sum(accuracy_list) / len(accuracy_list)

print(overall_accuracy)

def predict_author(text):
    model.eval()

    text_vector = vectorizer.transform([text])
    text_tensor = torch.tensor(text_vector.toarray(), dtype=torch.float32)

    with torch.no_grad():
        output = model(text_tensor).item()

        if output > 0.5:
            pred_index = 1
        else:
            pred_index = 0

        pred_author = encoder.inverse_transform([pred_index])[0]
        confidence = output if pred_index == 1 else 1 - output

    return f"Predicted Author: {pred_author}\nConfidence: {confidence:.4f}"

